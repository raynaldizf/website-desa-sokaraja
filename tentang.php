            Menjelaskan sejarah sosial sebuah kota kecil dengan segala dinamika sosialnya selama rentang waktu kurang
            lebih 175 tahun bukanlah pekerjaan yang ringan, jika bukan malah sebuah kemustahilan. Referensi dan data
            tentu saja adalah persoalan berat yang harus dipecahkan untuk bisa memberikan analisa komprehensif
            tentangnya. Di sisi lain, pilihan metode etnografis juga membawa persoalan sendiri karena keasyikan
            memberikan “thick description” seringkali melenakan keperluan untuk berjaga dan memberikan analisis kritis
            terhadap objek pengamatan.

            Studi ini adalah sebuah ikhtiar untuk memberikan penjelasan tentang kaitan erat antara institusi dan
            pandangan keagamaan dalam turut membentuk perubahan sosial ekonomi masyarakat. Dengan mengambil kasus
            gerakan tarekat, studi ini ingin membangun argumen bahwa agama bukan hanya merupakan ekspresi kesolehan
            personal tapi juga mencerminkan prestis cultural, status sosial dan pembedaan berbasis kelas dari para
            penganutnya. Disamping itu, agama membentuk kesadaran, kontruksi kognitif, dan juga sumber referensi
            tindakan individual dan kolektif dalam berhubungan dengan dunia material dan sosial.

            <span class="font-bold">Geertz dan Tesis Modjokuto Revisited: Sebuah Penafsiran (yang lain)</span>
            Upaya untuk memberikan penjelasan tentang kaitan erat antara spiritualitas dengan perkembangan dunia
            material adalah satu bidang yang bisa dikatakan sudah usang, tapi dengan relevansi yang sebenarnya bisa
            terus-menerus terbarui. Komodifikasi sosial yang dibawa oleh pasar ternyata tidak mengurangi peran agama
            dalam masyarakat, jika bukan malah memperkuatnya.

            Karya monumental Weber dalam The Protestant Ethic berangkat dari tesis tentang peran ide dalam turut
            membentuk perubahan sosial dan dinamika material. Karya lanjutnya dalam sosiologi agama memperkuat tesis
            dasarnya bahwa hanya reformasi protestanlah yang mampu menciptakan asketisme duniawi sebagai conditio sin
            qua non bagi berkembangnya rasionalitas instrumental ekonomi pasar kapitalistik. Berbeda dengan
            Protestanisme, karya perbandingannya tentang Islam dipenuhi oleh sikap skeptiknya tentang kemampuan Islam
            untuk menumbuhkan rasionalitas instrumental dan orientasi keduniawian bagi para pengikutnya. Menurutnya,
            Islam yang dipenuhi ajaran sufistik dan orientasi petualangan para warrior penyebar Islam mencegahnya untuk
            menumbuhkan asketisme duniawi sebagaimana yang terjadi di Kristen.

            Meskipun Geertz meminjam analisis Weber untuk menjelaskan perubahan sosial di Modjokuto, dia merevisi tesis
            Weber sekaligus memperkuatnya. Di satu sisi, Geertz tidak sepakat dengan Weber dan segala bias stereotyping
            yang terkandung dalam analisisnya tentang Islam. Di sisi lain, dia justru makin meneguhkan analisis Weber
            tentang peran rasionalitas instrumental sebagai syarat tumbuhnya semangat kewirausahaan dan perubahan
            sosial-ekonomi. Seperti halnya kasus gerakan Protestanisme yang diamati Weber, Geertz memberi apresiasi
            besar terhadap pengaruh reformasi Islam yang sedang tumbuh subur di Indonesia masa 1950-an dalam mendorong
            tumbuhnya sikap rasional di kalangan pengusaha Muslim perkotaan. Dalam analisisnya tentang perkembangan
            masyarakat urban di Modjokuto, Geertz mengamati proses transformasi sosial yang ditimbulkan oleh usaha
            sistematis kalangan pengusaha Muslim perkotaan untuk menciptakan bisnis yang efisien dan akumulasi modal.
            Terlepas dari empati personalnya terhadap proses perubahan itu, perangkat teori modernisasi yang dipakainya
            justru cenderung melemahkan dan mensimplifikasi proses yang sedang terjadi. Inilah yang membuat analisis
            orisinalnya tentang Jawa menyimpan paradoks.

            Sementara dia mengakui kemelekatan ekonomi pasar dalam sistem sosio-kultural yang lebih luas, Geertz juga
            percaya bahwa ide tentang pasar, sistem pertukaran, dan profit-making adalah sesuatu yang pada dasarnya
            asing bagi orang Jawa. Sementara dia menginsyafi bahwa perdagangan sedang tumbuh berkembang menjadi bagian
            integral dalam struktur ekonomi pribumi, Geertz juga percaya bahwa pertanian adalah mode produksi utama
            orang Jawa. Motif ideologis dari pengambilan kesimpulan semacam itu adalah di luar skup tulisan ini. Yang
            ingin penulis kemukakan adalah bahwa penjelasan bagi paradoks ini sebenarnya bisa dilacak dari kerangka
            argumennya yang lain tentang peran birokrasi dalam menciptakan ekonomi pasar yang rasionalistik.

            Dalam kerangka Weberian, institusi legal-rasional adalah determinan lain yang sangat penting bagi munculnya
            ekonomi kapitalistik. Institusi legal-rasional ini penting untuk menopang institusi pasar supaya berfungsi
            dengan baik. Dia berfungsi untuk menjamin terlaksananya perjanjian kontrak jual-beli, melindungi hak milik,
            menyediakan fasilitas publik, dan memberi hukuman bagi mereka yang melanggar aturan main ekonomi pasar.
            Lebih dari itu, institusi legal-rasional adalah produk agregat dari rasionalitas individual. Kerangka ini
            mengandaikan sebuah proses perubahan yang linier dari rasionalitas individu ke institusi legal-rasional.
            Rasionalitas individu membentuk rasionalitas kolektif dan rasionalitas kolektif tercermin dalam rasionalitas
            institusi politik dan birokrasi (baca: negara) untuk menelurkan kebijakan-kebijakan yang mendukung
            berfungsinya institusi pasar. Linieritas ini juga terlihat dari ramalannya tentang Indonesia masa depan.
            Baginya, rasionalitas individual dan institusional adalah prasyarat bagi apa yang dinamakan modernisasi.

            Ironisnya, temuan etnografis Geertz justru menunjukkan hal yang sebaliknya. Di satu sisi, gerakan reformasi
            Islam memang punya akar yang sangat kuat di kalangan usahawan pribumi perkotaan, sementara di sisi lain,
            birokrasi justru berkutat pada friksi politik berbasis aliran. Ini membuat birokrasi gagal mengemban fungsi
            idealnya sebagai penopang institusi pasar dan oleh karena itu juga bagi terlembaganya sistem ekonomi
            kapitalistik di Indonesia. Dengan kata lain, elaborasi Geertz tentang subkultur priyayi berangkat dari satu
            kesimpulan bahwa lembaga birokrasi di Indonesia lebih berorientasi pada pemapanan status daripada menopang
            institusi pasar untuk bekerja optimal.

            Terlepas dari berbagai kelemahannya, Geertz dkk. membuka salah satu tabir gelap yang menutupi Indonesia dan
            selama beberapa dekade menjadi lahan penyelidikan tak berkesudahan ahli-ahli filologi dan sosiolog Belanda.
            Untuk Geertz secara pribadi, penyelidikan etnografisnya di Modjokuto menjadi batu loncatan penting bagi
            pengukuhannya sebagai salah satu pendiri mazhab tersendiri dalam studi antropologi. Dalam kata pengantarnya
            untuk Agricultural Involution, ekonom terkemuka Benjamin Higgins mengemukakan bahwa temuan Geertz dan
            kawan-kawan membantu mempermudah kerja para ekonom yang konsen dengan persoalan pembangunan di Indonesia .
            Dalam konteks yang lebih luas, keseluruhan karya Modjokuto menjadi landasan penting bagi preskripsi
            pembangunan Indonesia masa Orba yang menekankan pada peran dominan modernizing elites dalam perubahan
            sosial.

            <span class="font-bold">Sekilas tentang Sokaraja</span>
            Sokaraja adalah sebuah kota kecil, sekitar 9 kilometer arah tenggara kota Purwokerto, Banyumas. Orang
            mengenal kota ini sebagai pusat jajanan khas Banyumasan, kripik dan getuk goreng. Sejak pertengahan 80-an,
            kota ini memang sudah kadung lekat dengan julukan sebagai kota kripik dan kota getuk. Nyatanya, julukan ini
            memang tidak berlebihan karena sepanjang jalan protol 1,5 kilometer yang membelah kota ini ke arah barat dan
            timur, berjejer puluhan kios dengan papan nama mencolok yang menawarkan merek dagangnya masing-masing.
            Setiap malam akhir pekan, akan banyak dijumpai puluhan bis antar kota dan mobil pribadi yang berjejer di
            sepanjang pinggir jalan, menunggu penumpangnya yang sedang berbelanja buah tangan jajanan khas ini.

            Sokaraja dibelah oleh aliran sungai (Kali Pelus) tepat di tengah, yang memisahkan kota ini menjadi wilayah
            utara (Sokara lor kali) dan selatan (Sokaraja kidul kali). Desa Sokaraja Lor dan Sokaraja Wetan di sebelah
            utara Kali Pelus lebih bercorak pertanian daripada desa Sokaraja Kidul, Tengah dan Kulon di selatan yang
            lebih bercorak kota . Pusat industri dan perdagangannya memusat di sepanjang empat ruas protokol jalan yang
            menghubungkan Sokaraja dengan kota-kota lainnya dan wilayah pemukimannya berhimpitan tepat di belalakang
            jejeran toko/kios. Sementara, lahan persawahan membentuk lingkaran terluar yang mengelilingi kota Sokaraja
            di delapan penjuru mata angin.

            Sokaraja adalah pusat pemukiman urban dan komersil pertama yang berkembang di wilayah Banyumas. Kota ini
            adalah distrik pertama yang mendapat suntikan modal Belanda pada waktu awal diberlakukannya tanam paksa
            (1840-1870). Konon cerita, dipilihnya Sokaraja banyak dipengaruhi oleh kolaborasi penguasa lokal Sokaraja
            saat itu (Tumenggung Jayadireja) dengan kumpeni Belanda pada saat Perang Diponegoro (1825-1830). Selagi
            daerah lain belum lagi beranjak dari isolasi geografis dan pertanian subsisten, Sokaraja sudah mendapatkan
            limpahan pembangunan fisik seperti jalan kereta api, jembatan, pasar, saluran irigasi, dan jalan raya.
            Singkatnya, gula menjadi mode ekonomi dominan di wilayah ini sampai dengan kebangkrutannya setelah depresi
            dunia akhir tahun 1920-an. Bangunan tua lapuk Pabrik Gula Kalibagor (500 meter selatan kota) yang berdiri
            sejak 1839 adalah saksi bisu kejayaan gula dalam ekonomi local Sokaraja selama rentang satu abad.

            Bagi orang luar, Sokaraja boleh jadi memang kadung lekat dengan getuk dan kripik. Tapi bagi penduduk asli,
            Sokaraja punya lebih banyak warna-warni sejarah dengan julukan yang berbeda-beda untuk tiap masa. Berbagai
            julukan itu menjadi pertanda aktivitas ekonomi yang berubah. Antara 1930-1960, Sokaraja dikenal sebagai kota
            batik. Dari awal Orba sampai akhir 80-an, kota ini dikenal dengan julukan kota lukisan dan kota keramik.
            Getuk goreng dan kripik hanya menandai perkembangan terbaru kegiatan ekonomi penduduknya. Entah julukan apa
            lagi yang akan muncul setelah ini.

            Kalaupun ada yang tidak berubah dari Sokaraja, itu karena dari dulu sampai sekarang orang mengenal kota ini
            sebagai kota santri. Kota kecil berpenduduk lebih kurang 22 ribu seluas kurang lebih 831 hektar ini memiliki
            7 buah pesantren, 3 di Sokaraja Lor, 3 di Sokaraja Kulon, dan 1 di Sokaraja Tengah. Pesantren tertua di kota
            ini, Pesantren Assuniyah Kebonkapol di Sokaraja Lor didirikan oleh Syekh Imam Rozi pada tahun 1830-an. Ke
            arah timur, tidak jauh dari Pesantren Assuniyah, ada pesantren besar khusus untuk pengikut tarekat, namanya
            Pondok Pesulukan Naqsabandi. Pesantren ini juga dinamakan Pondok Peguron. Pada bulan Rajab, Maulud dan
            Ramadhan, Pondok Peguron akan dibanjiri ribuan pengikut tarekat Naqsabandi dari seluruh Indonesia yang
            mengikuti ritual suluk akbar.

            Predikat sebagai kota santri ada hubungannya dengan sejarah gerakan Islam dan ketokohan beberapa kyainya.
            Sejak awal 1930-an, Sokaraja menjadi tempat kedudukan konsul tetap NU yang membawahi wilayah kerja Banyumas,
            Jogja, dan Kedu.[1] Melalui NU-lah, Sokaraja berkembang menjadi pusat perlawanan gerakan kyai menentang
            penjajahan Belanda dan Jepang. Menurut beberapa sumber lokal, gerakan perlawanan kyai ini merupakan bagian
            dari gerakan Mabadi Khoiro Ummah yang sudah dirintis oleh NU sejak awal 1930-an. Gerakan ini mencakup banyak
            kegiatan seperti pengajian rutin, pengumpulan dana perjuangan, pendirian madrasah, sampai dengan
            penggemblengan fisik untuk para pemuda.[2]

            Pada masa Jepang, gerakan perlawanan bawah tanah (non-cooperatie) ini berkembang menjadi gerakan cooperatie
            dengan penguasa pendudukan. Kyai Raden Mukhtar, konsul NU Banyumas misalnya, menjadi satu-satunya perwakilan
            NU di Syumuka (lembaga bentukan Jepang yang menyerupai lembaga kepenguluan masa Belanda di tingkat
            kabupaten) Dalam Guruku Orang-Orang Pesantren, Saifuddin Zuhri yang pernah menjadi tangan kanan Kyai Wahid
            Hasyim, menyinggung bahwa kerjasama ini merupakan bagian dari taktik baru yang ditempuh NU pasca penahanan
            Hadratus Syekh Hasyim Asy’ari oleh Jepang pada tahun 1943. Pada hemat penulis, perubahan strategi dari
            non-kerja sama ke kerjasama ini merupakan upaya NU untuk mengikis dominasi kalangan modernis dalam lingkaran
            birokrasi kolonial dan sekaligus menciptakan basis kekuasaan untuk dirinya sendiri dalam struktur baru pasca
            kolonial.

            Predikat sebagai kota santri untuk Sokaraja juga berasal dari ketokohan kyai-kyainya. Sampai saat ini,
            hampir semua kyai di Banyumas melacak genealogi pengetahuannya, langsung maupun tidak langsung, dari
            beberapa kyai sepuh di Sokaraja. Sokaraja menjadi tempat tinggal beberapa kyai generasi pertama di Banyumas
            yang mendapatkan pendidikannya dari pesantren-pesantren tua ternama seperti Termas, Bangkalan dan Lasem dan
            juga dari Arabia . Di samping itu, Sokaraja melahirkan kyai dari yang politisi, ahli fiqh, dan utamanya
            tarekat. Dari semuanya, barangkali tidak ada yang menyamai reputasi Kyai Saifuddin Zuhri yang pernah menjadi
            menteri agama era Soekarno akhir.

            <span class="font-bold">Tarekat dan Tradisi Islam Lokal</span>
            Tarekat adalah tradisi Islam lokal yang mengakar kuat di Banyumas secara umum dan khususnya Sokaraja.
            Barangkali ini ada kaitannya dengan sejarah awal perkembangan Islam di wilayah ini pada akhir abad ke-15.
            Sebelum kedatangan Islam, Banyumas adalah wilayah semi independen di bawah pengaruh Kerajaan Hindu Galuh
            Pakuan/Pajajaran. Empat abad setelah awal kedatangannya, Islam belumlah memiliki akar yang kokoh dalam
            tradisi keagamaan lokal. Beberapa sumber lisan (babad) bahkan menceritakan perlawanan yang dilakukan
            beberapa penguasa lokal terhadap proses Islamisasi di wilayah ini. Tarekat menjadi instrumen Islamisasi yang
            efektif karena kemampuannya mengakomodasi tradisi keagamaan lokal.

            Pada masa kekuasaan Tumenggung Jayadireja (1830-1853), Syattariyah adalah tarekat yang paling tersebar luas
            di Sokaraja. Diperkirakan, tarekat ini bersumber dari murid-murid Syekh Abdul Mukhyi, Garut, seorang mursyid
            tarekat Syattariyah yang mendapatkan ijazah irsyad-nya dari Syekh Abdurrauf Singkel, Aceh. Di Banyumas,
            Syattariyah menciptakan varian baru yang menggabungkan beberapa ajaran tarekat lain, seperti Rifaiyah dan
            Naqsabandi-Qodiriya h. Tarekat ini dikenal dengan nama tarekat Akmaliyah/Kamaliyah .

            Ajaran tarekat Akmaliyah pernah menjadi objek disertasi Drewes pada tahun 1925 di Leiden . Menurut Drewes,
            Akmaliyah disebarkan oleh tiga guru tarekat; Kyai Hasan Maulani (Lengkong, Cirebon ), Kyai Nurhakim (Pasir
            Wetan, Purwokerto), dan Malangyuda (Rajawana, Purbalingga) . Ketiganya merupakan guru-murid, dengan Kyai
            Hasan Maulani sebagai pendiri dan guru utamanya. Menariknya, Kyai Hasan Maulani sendiri mendapat bimbingan
            pertama kali untuk masuk tarekat dari Syekh Abdussomad, seorang guru Naqsabandi-Qodiriya h di desa Jombor,
            sekitar 25 km barat Purwokerto. Sehingga tidak berlebihan untuk menyatakan bahwa Akmaliyah adalah tarekat
            lokal asli Banyumas. Mendasarkan pada studi Drewes, Bruinessen dan Steenbrink menyatakan bahwa Akmaliyah
            merupakan tarekat yang kental dengan ajaran wahdatul wujud dan sinkretisme Jawa.

            Banyaknya pengikut tarekat Akmaliyah menakutkan penguasa saat itu. Hal ini mendorong Belanda membuang Kyai
            Hasan Maulani ke Menado pada tahun 1846.[3] Sepeninggalnya, ajarannya dilanjutkan oleh Kyai Nurhakim dan
            Malangyuda. Pada masa kepemimpinan Kyai Nurhakim dan Malangyuda, Akmaliyah menginspirasi beberapa
            pemberontakan rakyat di wilayah ini. Ini mendorong Belanda mengawasi ketat gerak-gerik kedua orang ini.

            Seorang sejarawan lokal yang pernah menulis tentang ini, Tanto Sukardi, menghubungkan gerakan tarekat
            Akmaliyah dengan meluasnya ketidakpuasan rakyat terhadap kondisi sosial ekonomi masa tanam paksa. Tarekat
            bahkan memakan korbannya dari kalangan elit penguasa ketika Belanda mengasingkan bekas sekutunya, Tumenggug
            Jayadireja (bekas penguasa Sokaraja dan Bupati Purwokerto, 1853-1860) ke Padang pada tahun 1860. Bupati
            Jayadireja dituduh berkomplot untuk menggulingkan kekuasaan Belanda karena asosiasinya dengan gerakan
            tarekat Kyai Nurhakim.

            Surutnya Syattariyah dan Akmaliyah di Banyumas secara umum dan Sokaraja secara khusus tidak mematikan
            gerakan tarekat di wilayah ini. Sejak awal 1880-an, Kyai Muhammad Ilyas muncul sebagai mursyid terkemuka.
            Dia merupakan salah satu khalifah Sulaiman Zuhdi (guru tarekat Naqsabandi-Kholidiy ah asal Turki di Mekkah)
            untuk wilayah Jawa.[4] Dia mulai menyebarkan tarekat ini dari langgar kecilnya di dukuh Kedungparuk, Mersi
            (sekitar 5 km timur Purwokerto). Mengulang nasib guru-guru tarekat sebelumnya, sambutan luas masyarakat
            terhadap ajarannya kembali mengundang kecurigaan Belanda terhadap Kyai Muhammad Ilyas. Dengan tuduhan makar,
            Kyai Muhammad Ilyas kemudian ditahan Belanda pada tahun 1888.[5]

            Setelah dibebaskan dari tahanan, Kyai Muhammad Ilyas meneruskan ajaran tarekatnya di Sokaraja Lor. Pemilihan
            Sokaraja Lor erat kaitannya dengan pengawasan ketat Belanda yang terus berlanjut atas tokoh ini. Secara
            geografis, Sokaraja dipilih karena mempunyai jalur transportasi yang bagus dan hanya berjarak kurang dari 10
            km dari kota Banyumas, ibukota karesidenan Banyumas saat itu. Alasan lainnya lebih bersifat pribadi. Kyai
            Muhammad Ilyas hanya diijinkan mengajar tarekat dari masjid wakaf Penghulu landraat Abubakar, seorang
            pejabat agama kolonial yang belakangan menjadi mertuanya. Pada waktu meninggalnya tahun 1914, Kyai Muhammad
            Ilyas meninggalkan pada keturunannya sebuah jaringan tarekat yang tersebar luas di wilayah Banyumas dan
            sekitarnya.[6] Kyai Abdussalam, mursyid Naqsabandi-Kholidiy ah saat ini, adalah cicit Kyai Muhammad Ilyas.
            Di bawah Kyai Abdussalam, jaringan tarekat ini menyebar sampai ke Aceh, Riau, Jambi, Lampung, Jabar, Kalsel
            dan Kaltim, dan Kalteng dengan jamaah sekitar 35.000 orang.

            Sejak akhir 1920-an, dominasi Naqsabandi-Kholidiy ah di Sokaraja mendapat saingan dari tarekat Syadziliyah.
            Tarekat ini disebarkan pertama kali oleh Kyai Muhammad Asfiya, Sokaraja Tengah, cucu dari Syekh Imam Rozi,
            perintis dakwah Islam di Sokaraja dan pendiri Pesantren Assuniyah, Kebonkapol Sokaraja Lor. Saat ini,
            terdapat lima orang mursyid di kota Sokaraja; yaitu Kyai Muhammad Hidayat, Habib Umar Bafakih, Kyai
            Abdussalam, Habib Khusen bin Salim dan Kyai Muhammad Imam Munchasir. Dua yang pertama adalah mursyid
            Naqsabandi-Qodiriya h sedang dua yang terakhir adalah mursyid Syadziliyah. Syadziliyah adalah jaringan
            tarekat tunggal terbesar di wilayah ini dengan jumlah mursyid sebanyak kurang lebih 24 orang.

            Tarekat dan Ekonomi Batik
            Sejak kolapsnya industri gula pada akhir 1920-an, batik telah menggantikannya menjadi penggerak utama
            ekonomi lokal Sokaraja. Ada dua pandangan tentang asal-usul batik di Banyumas. Satu pandangan menyatakan
            bahwa batik berkembang sejak lama di daerah-daerah perdikan di Banyumas. Pandangan yang lain menyatakan
            bahwa keahlian membatik ini dibawa oleh para pengikut Diponegoro yang melarikan diri ke daerah Banyumas
            setelah kekalahannya dalam perang Jawa pada tahun 1830. Terlepas dari pandangan mana yang benar, batik
            menandai kebangkitan usahawan Muslim pribumi. Pada masa keemasannya antara pertengahan 1950-an dan 1960-an,
            Sokaraja menyumbang sebagian besar produksi Batik Banyumas.

            Kejayaan perniagaan saudagar batik Sokaraja disumbang oleh kebijakan negara untuk mengembangkan pengusaha
            pribumi. Di bawah kebijakan ini, koperasi batik Banyumas (Perbain) yang masuk sebagai salah satu anggota
            pendiri GKBI (Gabungan Koperasi Batik Indonesia ) mendapatkan hak monopoli pengadaan mori dan alat-alat
            pembantikan. Mengikuti kolega-koleganya di GKBI, Perbain mendulang keuntungan besar besar dari monopoli ini.
            Pada tahun 1956, jumlah pengusaha batik anggota Perbain berjumlah 108. Pada masa keemasannya, Perbain bukan
            saja berkutat pada usaha pembuatan dan penjualan batik tapi juga bergerak di bidang sosial. Mereka mengelola
            sekolah, pusat kesehatan masyarakat dan pabrik tekstil.

            Dinamika gerakan tarekat di Sokaraja berkaitan erat dengan perkembangan pengusaha batik pribumi. Jaringan
            bisnis para pengusaha batik ini biasanya paralel dengan jaringan keagamaan dan sosial gerakan tarekat. Di
            Sokaraja bahkan ada kecenderungan menarik karena para guru tarekat biasanya adalah juga para pengusaha batik
            yang sukses. Kyai Rifai misalnya, adalah contoh pengusaha batik kaya yang juga seorang guru tarekat dengan
            jaringan yang sangat luas. Dia adalah guru tarekat Naqsabandi-Kholidiy ah yang didirikan oleh kakeknya, Kyai
            Muhammad Ilyas. Dia menggantikan ayahnya (Kyai Affandi) sebagai guru tarekat sejak 1928 sampai meninggalnya
            pada tahun 1968. Pada masa hidupnya, Kyai Rifai konon adalah salah seorang paling kaya di Sokaraja. Jaringan
            tarekat dan bisnis Kyai Rifai menyebar dari Ciamis, Garut, Tasikmalaya, Purbalingga, Banjarnegara,
            Pekalongan, Pemalang, Temanggung, dan Magelang.

            Seperti halnya Naqsabandi-Kholidiy ah, Syadziliyah, tarekat terbesar kedua di Sokaraja juga memiliki
            jaringan sosial yang paralel dengan jaringan perniagaan saudagar batik. Genealogi tarekat Syadziliyah
            Sokaraja dan Banyumas berhubungan dengan para saudagar batik di Laweyan, Solo. Bukan satu kebetulan bahwa
            guru-guru Syadziliyah dengan jaringan terbesar di Banyumas mendapat ijazahnya dari guru tarekat sekaligus
            saudagar batik dari Solo, seperti Kyai Idris, Kyai Siroj dan Kyai Ma’ruf.

            Pada awal 1960-an, kemapanan para saudagar batik ini sedikit terusik seiring dengan radikalisasi politik
            karena menguatnya komunisme. Pada pemilu lokal tahun 1957, PKI muncul sebagai kekuatan politik terbesar
            ketiga setelah PNI dan NU. Dengan jumlah massa sekitar 200,000 orang, PKI adalah kekuatan yang cukup
            diperhitungkan di Banyumas saat itu. Dengan komposisi politik demikian, tidaklah mengherankan jika Banyumas
            pun tidak luput dari pertumpahan darah tahun 1965. Sokaraja boleh dikatakan malah termasuk terlibat secara
            emosional dalam drama pertumpahan darah saat itu karena salah satu putra daerahnya, Jenderal Soeprapto,
            adalah salah satu korban yang diculik dan dibunuh di Lubang Buaya.

            Seperti juga koleganya di Jawa timur, Banser termasuk tulang punggung dalam proses pembersihan orang-orang
            yang diduga komunis. Sokaraja pun bukan pengecualian. Pada masa itu, Sokaraja Wetan dan Sokaraja Kidul
            merupakan basis terkuat PKI. Tokoh-tokohnya adalah para pegawai kereta api di Stasiun Sokaraja. Pak Iskandar
            Tirta Brata, tokoh Sokaraja Wetan dan kebetulan yang pada saat itu menjadi Panglima Banser Banyumas, memberi
            kesaksian bahwa di Banyumas, pertumpahan darah bisa segera diredam oleh para kyai.[7] Ini tidak berarti
            bahwa tidak ada korban dari kalangan komunis, tapi untuk Sokaraja secara khusus dan Banyumas secara umum,
            skalanya tidaklah semasif seperti di daerah lain. Sayangnya, tidak ada catatan pasti berapa jumlah korban
            yang tewas dari kalangan komunis pada saat itu. Reportase seorang jurnalis Australia yang dimuat di
            Economist pada bulan Oktober 1965 tentang pembersihan komunis di Banyumas juga hanya menyinggung sekilas
            tentang metode pembunuhan dan penculikan atas mereka yang diduga anasir komunis tapi tidak menyebutkan
            berapa jumlah korban.

            Peristiwa pembersihan pengikut komunis di Sokaraja mungkin ada kaitannya dengan konflik antara lingkaran
            kemapanan para saudagar batik dan pengikut komunis yang biasanya para petani miskin. Sumber konfliknya
            biasanya adalah soal tanah. Hal ini karena saudagar batik biasanya dikenal juga sebagai para tuan tanah.
            Mereka menguasai lahan-lahan pertanian yang luas sebagai hasil dari akumulasi keuntungan yang mereka
            dapatkan dari perniagaan batik. Sampai sekarangpun, persoalan tanah adalah persoalan sensitif di Sokaraja,
            terutama di daerah Sokaraja Kidul dan Wetan yang kebetulan juga menjadi benteng kaum abangan.

            Kejayaan para pengusaha batik Sokaraja berakhir sejak naiknya Orde pada pertengahan dekade 1960-an. Hal ini
            karena para teknokrat yang menjadi tulang punggung perumusan kebijakan ekonomi saat itu segera mencabut
            berbagai privilege yang dinikmati GKBI. Kebijakan pro pasar ala teknokrat ini segera memukul para pengusaha
            batik Sokaraja. Dihadapkan pada iklim yang baru, mereka tidak mampu bersaing menghadapi serbuan impor
            tekstil murah yang membanjiri Indonesia sejak akhir 1960-an. Mereka semakin tenggelam ketika pemerintah juga
            menggalakkan pengembangan pabrik-pabrik tekstil sebagai bagian dari kebijakan ISI yang menjadi favorit sejak
            1973.

            Sekarang ini hanya tersisa 52 pengrajin batik di Sokaraja yang sekedar bertahan hidup daripada mencari
            keuntungan. Koperasi Batik Perbain memang masih memiliki gedung megah 2 lantai di Sokaraja Tengah, tapi
            skala usaha yang mereka kelola sudah jauh merosot dari pada era keemasan tahun 1950-an.

            <span class="font-bold">Islamisasi dan Rasionalisasi Status</span>
            Pandangan Geertz tentang Islam Jawa berangkat dari asumsi bahwa hanya reformasi agamalah (baca: modernisme
            Islam) yang mempunyai potensi untuk mengembangkan sikap rasionalitas instrumental bagi bekerjanya institusi
            pasar dan birokrasi. Argumennya berangkat dari pembedaannya antara dua kelompok sosial; status group dan
            market-oriented group di mana modernisme Islam menjadi sumber referensi tindakan bagi kelompok yang
            berorientasi pasar, sedang elemen non-Islam menopang kemapanan kelompok status. Dalam kerangka ini, kelompok
            berorientasi pasar dianggap lebih rasional daripada kelompok status dalam mengeksploitasi kesempatan yang
            disediakan oleh berkembangnya ekonomi pasar.

            Namun demikian, trikotomi kelompok sosial (abangan, santri, priyayi) dan dikotomi pandangan keagamaan (Islam
            vs. Hindu-Budha) dalam tesis Geertz gagal menjelaskan dua hal; 1. pluralitas keagamaan di kalangan
            masyarakat Jawa, dan 2. dinamika sosial-ekonomi masyarakatnya. Konsekuensi dari dua hal ini, tesis Geertz
            memiliki kelemahan mendasar untuk menjelaskan kaitan antara agama dan tindakan sosial para pengikutnya, hal
            yang menjadi konsen utama karyanya tentang Jawa.

            Islamisasi yang terus berlanjut di tengah melembaganya pasar kapitalistik adalah adalah dua hal yang harus
            dijelaskan untuk meruntuhkan tesis Modjokuto-nya Geertz. Kasus Sokaraja menunjukkan bahwa represi politik
            masa kolonial, politik kerjasama pada masa Jepang, dan patronase negara pasca kemerdekaan menjadi preseden
            sejarah yang sangat mempengaruhi proses Islamisasi lanjut, hubungan penguasa-rakyat, dan perkembangan
            sosial-ekonomi di wilayah ini dimana tarekat tetap memainkan peran sebagai kesadaran kolektif untuk
            menciptakan integrasi sosial dan mengelola konflik.

            Praktek beragama ala Sufi, pragmatisme dalam politik dan etik ekonomi produktif banyak mewarnai kesadaran
            dan menjadi referensi tindakan orang dalam hubungan sosialnya. Sayangnya, kesadaran kolektif absolut seperti
            tarekat tidak memberi ruang bagi munculnya perbedaan dan radikalisasi. Potensi tarekat dalam mendorong
            perubahan oleh karenanya menyimpan ambiguitas, karena alih-alih mendorong munculnya rasionalitas
            instrumental ekonomi pasar yang kapitalistik, dia justru menciptakan justifikasi untuk menopang
            kelompok-kelompok status yang mapan secara ekonomi.

